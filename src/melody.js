  // Called every beat from Sequencer.tick (SPEC_023 §B2: sub-beat scheduling)
  tick: function(beatTime) {
    if (!this._active || !audioCtx || this._muted || !this._palette) return;

    var phase = G.phase || 'pulse';
    var density = this._PHASE_DENSITY[phase] || this._PHASE_DENSITY.pulse;

    // Silent in Pulse
    if (density.gain === 0) return;

    // Octave drop countdown
    if (this._octaveDrop > 0) this._octaveDrop--;

    // --- Play any queued grace notes (from graze events) before main note ---
    this._playGraceNotes(beatTime);

    // How many sub-positions per beat based on genre rhythm config
    var subsPerBeat = (this._subdivide === '16th') ? 4 : (this._subdivide === '8th') ? 2 : 1;
    var beatDurSec = 60 / G.bpm;
    var subDurSec = beatDurSec / subsPerBeat;

    // Track bar position for rhythmic rest alignment (4 beats per bar)
    this._barBeat = (this._barBeat + 1) % 4;
    if (this._barBeat === 0) this._barBeat = 0; // wrap

    // Process each sub-position in this beat
    for (var sub = 0; sub < subsPerBeat; sub++) {
      var subTime = beatTime + sub * subDurSec;

      // Apply groove timing if inherited (SPEC_023 §B3)
      if ((this._swingInherit || this._humanizeInherit) && typeof GrooveEngine !== 'undefined') {
        var stepIdx = this._melodyStep % 16;
        var grooveOffset = GrooveEngine.getTimingOffset(
          this._swingInherit ? stepIdx : 0,  // pass 0 for even steps if no swing
          subDurSec
        );
        // Only apply humanize if humanizeInherit, swing is already conditional via stepIdx
        if (!this._humanizeInherit) {
          // Strip humanize by only keeping swing component (re-call without randomness not possible,
          // so accept humanize is bundled — in practice humanizeInherit tracks swingInherit)
        }
        subTime += grooveOffset;
      }

      // Advance melody step counter (for groove step tracking)
      this._melodyStep++;

      // --- Melodic rhythm extensions (SPEC_036 §8) ---
      // Syncopation: shift note early by half a sub-unit
      if (this._syncopationProbability > 0 && (_songRng || Math.random)() < this._syncopationProbability) {
        var syncShift = subDurSec * 0.5;
        // Clamp: don't go before the beat boundary
        subTime = Math.max(beatTime, subTime - syncShift);
      }
      // Dotted compensation: previous note was dotted, shorten this one by 0.5×
      // (stored as a duration scalar on the note; tracked via _dottedCompensate flag)
      // Dotted bias: make this note 1.5× duration (next note compensates)
      // Both are applied in _playMelodyNote via the subDurSec hint — we pass a modified value
      var noteDurSec = subDurSec;
      if (this._dottedCompensate) {
        noteDurSec = subDurSec * 0.5;
        this._dottedCompensate = false;
      } else if (this._dottedBias > 0 && (_songRng || Math.random)() < this._dottedBias) {
        noteDurSec = subDurSec * 1.5;
        this._dottedCompensate = true;
      }
      // Rubato: ±12.5ms freeform drift (independent of swing grid)
      if (this._rubato) {
        subTime += ((_songRng || Math.random)() - 0.5) * 0.025;
      }

      // --- Rest between phrases ---
      if (this._restBeats > 0) {
        // Decrement rest in sub-beat units
        this._restBeats--;
        continue;
      }

      // --- Need a new phrase? ---
      if (this._phrasePos >= this._currentPhrase.length) {
        this._generatePhrase();
        this._phrasePos = 0;
        if (this._currentPhrase.length === 0) {
          this._restBeats = subsPerBeat * 2; // 2 beats worth of sub-rest
          continue;
        }
      }

      // --- Hold check: skip this sub-position, note rings longer (SPEC_023 §B2) ---
      if (sub > 0 && (_songRng || Math.random)() < this._holdProbability) {
        continue; // previous note rings through this sub-position
      }

      // --- Play next note in phrase (re-validate against current chord) ---
      var midiNote = this._currentPhrase[this._phrasePos];
      this._phrasePos++;

      midiNote = this._revalidateNote(midiNote);

      // Pass sub-beat duration hint for envelope scaling (noteDurSec reflects dotted compensation)
      this._playMelodyNote(midiNote, subTime, undefined, noteDurSec);

      // --- End of phrase: schedule rest ---
      if (this._phrasePos >= this._currentPhrase.length) {
        var rr = density.restRange;
        var restSubs;
        if (this._restStyle === 'rhythmic') {
          // Bar-aligned: snap rest to next downbeat or half-bar (SPEC_023 §B4)
          restSubs = this._calcRhythmicRest(rr, subsPerBeat, sub, subsPerBeat);
        } else {
          // Even: random rest in sub-beat units
          var restBeats = rr[0] + Math.floor((_songRng || Math.random)() * (rr[1] - rr[0] + 1));
          restSubs = restBeats * subsPerBeat;
        }
        this._restBeats = restSubs;
      }
    }
  },

  // Bar-aligned rest calculation (SPEC_023 §B4)
  // Returns rest duration in sub-beat units, snapped to bar/half-bar boundary
  _calcRhythmicRest: function(restRange, subsPerBeat, currentSub, totalSubsThisBeat) {
    var subsPerBar = subsPerBeat * 4;
    var subsPerHalfBar = subsPerBeat * 2;

    // How many subs until next bar or half-bar boundary?
    var currentBarPos = ((this._barBeat - 1) * subsPerBeat + currentSub + 1) % subsPerBar;
    if (currentBarPos < 0) currentBarPos += subsPerBar;

    var toNextBar = (subsPerBar - currentBarPos) % subsPerBar;
    if (toNextBar === 0) toNextBar = subsPerBar;
    var toNextHalf = (subsPerHalfBar - (currentBarPos % subsPerHalfBar)) % subsPerHalfBar;
    if (toNextHalf === 0) toNextHalf = subsPerHalfBar;

    var minSubs = restRange[0] * subsPerBeat;
    var maxSubs = restRange[1] * subsPerBeat;

    // Prefer half-bar if it's in range, else next bar
    if (toNextHalf >= minSubs && toNextHalf <= maxSubs) return toNextHalf;
    if (toNextBar >= minSubs && toNextBar <= maxSubs) return toNextBar;
    // Extend to next bar + half if needed
    var toNextBarPlusHalf = toNextBar + subsPerHalfBar;
    if (toNextBarPlusHalf >= minSubs && toNextBarPlusHalf <= maxSubs) return toNextBarPlusHalf;

    // Fallback: random within range (rare edge case)
    return minSubs + Math.floor((_songRng || Math.random)() * (maxSubs - minSubs + 1));
  },

  // ── Grace note from graze event (SPEC_017 §5) ────────────────────────────
  // Adds a 16th-note ornament before the next melody note
  queueGraceNote: function() {
    if (!this._active) return;
    // Max 2 queued graces to prevent stacking
    if (this._graceQueue.length < 2) {
      this._graceQueue.push(true);
    }
  },

  // ── Energy loss: drop octave + halve volume for 4 beats (SPEC_017 §5) ─────
  onHit: function() {
    this._octaveDrop = 4;
  },

  // ── Phase transition: play resolution phrase (SPEC_017 §5) ────────────────
  onPhaseChange: function(newPhase, beatTime) {
    if (!this._active || !audioCtx) return;
    // Resolution: quick ascending scale run to tonic
    if (typeof HarmonyEngine !== 'undefined') {
      var scaleNotes = HarmonyEngine.getScaleNotes(5);
      if (scaleNotes && scaleNotes.length >= 3) {
        var beatDur = 60 / G.bpm;
        var subDur = beatDur / 4; // 16th note
        // Play last 3 scale notes ascending as resolution
        var start = Math.max(0, scaleNotes.length - 3);
        for (var i = start; i < scaleNotes.length; i++) {
          var t = beatTime + (i - start) * subDur;
          this._playMelodyNote(scaleNotes[i], t, 0.7);
        }
      }
    }
  },

  // ── Intensity interaction: longer phrases at high intensity ─────────────
  updateIntensity: function(intensity) {
    // At intensity 20+, phrases can be longer (up to +2 notes)
    if (intensity >= 20) {
      this._phraseLenBonus = Math.min(2, Math.floor((intensity - 20) / 15));
    } else {
      this._phraseLenBonus = 0;
    }
  },

  shutdown: function() {
    this._active = false;
    this._currentPhrase = [];
    this._graceQueue = [];
    if (this._voice) {
      try { this._voice.stop(); } catch (e) { /* already stopped */ }
      this._voice = null;
    }
    if (this._voiceGain) {
      try { this._voiceGain.disconnect(); } catch (e) { /* ok */ }
      this._voiceGain = null;
    }
    console.log('[MelodyEngine] shutdown');
  },

